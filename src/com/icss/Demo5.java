package com.icss;

public class Demo5 {

	public static void main(String[] args) {
		// 整敌
		byte b1=-123;
		System.out.println("字节型："+b1);
		long L =10000000000l;
		System.out.println("长整型："+L);
		short i =100;
		System.out.println("java中默认是int:"+i);
		float f =1.21f;
		System.out.println("单精度浮点型："+f);
		double d =1.21;
		System.out.print("双精度浮点型："+d);
		double d1=3;
		System.out.println(d1-2);
		char c='5';
		System.out.println("字符："+c);
		char c2=97;
		System.out.println(c2);


	}

}

package com.icss;

public class Demo6 {

	public static void main(String[] args) {
		//数据类型的转换
		byte b =10;
		short s =b;
		System.out.println(s);
		char c ='a';
		int i =c;
		System.out.println(i);
		float f =i;
		System.out.println(f);
		
		int n =10;
		byte b2=(byte)n;
		System.out.println(b2);

	}

}
